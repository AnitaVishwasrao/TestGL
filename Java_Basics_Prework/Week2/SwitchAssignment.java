import java.util.Scanner;

public class SwitchAssignment{
    public static void main(String []args){
        int num = 2;

        switch(num){
            case 1:
            System.out.println("Neither prime nor composite");break;
            case 2:
            System.out.println("Number is prime");break;
            case 4:
            System.out.println("Number is composite");break;
        }
    }
}